using UnityEngine;

public class MusicLoop : MonoBehaviour
{
    private static MusicLoop instance = null;

    void Awake()
    {
        if (instance == null)
        {
            instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else if (instance != this)
        {
            Destroy(gameObject);
        }
    }
}
